import java.util.Random;

public class Dice {
    private int diceValue;

    /*////////////////////////////////////////////////////////////////////////////////
    Roll function seeds a random number from 1-6
    *///////////////////////////////////////////////////////////////////////////////*/
    public void roll(){
        diceValue = new Random().nextInt(6)+1;
    }

    /*////////////////////////////////////////////////////////////////////////////////
    Method returns dice value
    *///////////////////////////////////////////////////////////////////////////////*/
    public int getDiceValue(){
        return diceValue;
    }
}
